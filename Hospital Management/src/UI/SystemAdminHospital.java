 public void PopulateTable()
    {
        
        DefaultTableModel model=(DefaultTableModel) tblHospital.getModel();
        model.setRowCount(0);
        for(Hospital h: hd.getHospitalDirectory())
        {
            System.out.print(h.getName());
             Object[] row=new Object[9];
             row[0]=h.getName();
             row[1]=h.getAddress();
             row[2]=h.getCity();
             row[3]=h.getCommunity();
             row[4]=h;
             model.addRow(row);
        }
}
