    private void txtAgeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAgeKeyReleased
        // TODO add your handling code here:
         String age =txtAge.getText();
        if (age.length()>3)
        {
            JOptionPane.showMessageDialog(this,"Enter Valid Age.");
            txtAge.setText("");
            
        }
        if (!isNumeric(age)& !age.equals(""))
        {
            JOptionPane.showMessageDialog(this, "Invalid Age");
            txtAge.setText("");
        }
    }//GEN-LAST:event_txtAgeKeyReleased

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        for (Patient p: pd.getPatientDirectory())
        {
            if (p.getPatientID()==loggedPatient)
            {
                txtName.setText(p.getName());
                txtpId.setText(String.valueOf( p.getPatientID()));
                txtAge.setText(String.valueOf(p.getAge()));
                txtCity.setText(p.getCity());
                txtCommunity.setText(p.getCommunity());
                txtGender.setText(p.getGender());
                txtphNo.setText(String.valueOf(p.getPhNo()));
                txtResidence.setText(p.getResidence());
            }
        }
    }//GEN-LAST:event_formWindowOpened
 public static boolean isNumeric(String str) { 
  try {  
    Double.parseDouble(str);  
    return true;
  } catch(NumberFormatException e){  
    return false;  
  }  
    }
